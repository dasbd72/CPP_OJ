#include<stdio.h>
#include<math.h>
#define MAX 1000007
int R[100007][3][3]={};
int main(){
	R[1][0][0]=1,R[1][1][1]=1,R[1][2][2]=1;
	for(int i=2;i<=100000;i++){
		R[i][0][0]=R[i-1][0][0]+R[i-1][0][1]+R[i-1][0][2];
		R[i][0][1]=R[i-1][0][0]+R[i-1][0][1];
		R[i][0][2]=R[i-1][0][0]+R[i-1][0][2];
		R[i][1][0]=R[i-1][1][0]+R[i-1][1][1]+R[i-1][1][2];
		R[i][1][1]=R[i-1][1][0]+R[i-1][1][1];
		R[i][1][2]=R[i-1][1][0]+R[i-1][1][2];
		R[i][2][0]=R[i-1][2][0]+R[i-1][2][1]+R[i-1][2][2];
		R[i][2][1]=R[i-1][2][0]+R[i-1][2][1];
		R[i][2][2]=R[i-1][2][0]+R[i-1][2][2];
		R[i][0][0]%=MAX;R[i][0][1]%=MAX;R[i][0][2]%=MAX;
		R[i][1][0]%=MAX;R[i][1][1]%=MAX;R[i][1][2]%=MAX;
		R[i][2][0]%=MAX;R[i][2][1]%=MAX;R[i][2][2]%=MAX;
	}
	int T,N;
	for(scanf("%d",&T);T;T--){
		scanf("%d",&N);
		printf("%d\n",(R[N][0][0]+R[N][0][1]+R[N][0][2]+R[N][1][0]+R[N][1][1]+R[N][2][0]+R[N][2][2])%MAX);
	}
}
